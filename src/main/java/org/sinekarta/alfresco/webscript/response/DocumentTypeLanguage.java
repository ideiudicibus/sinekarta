package org.sinekarta.alfresco.webscript.response;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "documentTypeLanguage")
@XmlAccessorType(XmlAccessType.FIELD)
public class DocumentTypeLanguage {
	
	private String code;
	
	private String description;

	public DocumentTypeLanguage() {
		super();
	}

	public DocumentTypeLanguage(String code, String description) {
		super();
		this.code = code;
		this.description = description;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
}
